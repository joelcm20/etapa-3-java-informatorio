public class Caracteres {

    public static void main(String[] args) {
        char j = 'j';
        char o = 'o';
        char e = 'e';
        char l = 'l';
        System.out.println(j);
        System.out.println(o);
        System.out.println(e);
        System.out.println(l);

        /*
         * ¿Por qué no puede usar "" en el char?
         * 
         * Esto se debe a que las comillas simples se utilizan para denotar caracteres
         * individuales, mientras que las comillas dobles se utilizan para denotar
         * cadenas de caracteres.
         * Cuando colocas un carácter entre comillas simples en Java, estás creando una
         * instancia de la clase char, que representa un único carácter Unicode.
         */
    }
}