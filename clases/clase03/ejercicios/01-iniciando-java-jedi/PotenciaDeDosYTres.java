public class PotenciaDeDosYTres {
    public static void main(String[] args) {
        int numero = 2;
        int potenciaDeDos = numero * numero;
        int potenciaDeTres = numero * numero * numero;
        System.out.printf("%d al cuadrado es: %s\n", numero, potenciaDeDos);
        System.out.printf("%d al cubo es: %s\n", numero, potenciaDeTres);
    }
}